#include "libft.h"

char	*ft_putw(const char *s, char *dst, char c, int i)
{
	unsigned	j;

	j = 0;
	while (s[i] != c && s[i] != '\0')
	{
		dst[j] = s[i];
		j++;
		i++;
	}
	dst[j] = '\0';
	return (dst);
}
