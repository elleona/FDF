#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	unsigned	i;

	i = 0;
	if (s == NULL)
		return ;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
