#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	unsigned	i;

	i = 0;
	if (s == NULL)
		return ;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
}
